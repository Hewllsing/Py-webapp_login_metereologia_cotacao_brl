# Lista de números que serão somados
numero = [10, 20, 30, 40, 50]  # A soma deve resultar em 150

# Variável que armazenará o resultado final da soma
resultado = 0

# Percorre cada valor da lista 'numero'
for i in numero:
    # Soma o valor atual à variável 'resultado'
    resultado += i

# Exibe o resultado da soma final
print(f"O resultado é: {resultado}")
